package curso_algaworks_basic;

public class TiposNumericos {
  public static void main (String[] args){
    Byte numeroByte = 127; // -128 até 127

    Short numeroShort = -32768; // -32768 até 32767

    Integer numeroInteger = 214783647; // -2147483648 até 2147483647

    Long numeroLong = 1L; // -9,223372037 até 9,2223372037x10

    Float numeroFloat = 1.0f;

    Double numeroDouble = 1.0;
  }
  
}
